<h4>ما هي ملفات تعريف الارتباط؟</h4>
<p>
    "ملفات تعريف الارتباط" هي ملفات نصية صغيرة يمكن وضعها على جهاز الكمبيوتر الخاص بك أو جهازك المحمول لتحديد متصفح الويب الخاص بك ونشاطات جهاز الكمبيوتر الخاص بك على خدمة الزا بوت ومواقع الويب الأخرى.
</p>
<h4>ما هي علامة الصفحة؟</h4>
<p>
    "علامات الصفحة"، المعروفة أيضًا باسم علامات الويب أو علامات gif، هي تكنولوجيا ويب تستخدم لتتبع معلومات استخدام موقع الويب أو البريد الإلكتروني، مثل عدد مرات ظهور صفحة معينة أو البريد الإلكتروني. علامات الصفحة غير مرئية بالنسبة لك، ويمكن أن يحتوي أي جزء من خدمة الزا بوت، بما في ذلك الإعلانات، أو البريد الإلكتروني الذي يتم إرساله نيابة عنا، على علامات الصفحة.
</p>
<h4>هل تستخدم الزا بوت ملفات تعريف الارتباط، ولماذا؟</h4>
<p>
    نعم. نحن نستخدم ملفات تعريف الارتباط لتخصيص تجربتك على موقع الويب الخاص بالزا بوت، للسماح لنا بمراقبة كيفية استخدامك لخدمة الزا بوت إحصائيًا حتى نتمكن من تحسين عروضنا، ولتوجيه بعض الإعلانات إلى متصفحك قد تكون مهتمًا بها، أو لتحديد شعبية محتوى معين.
    باستخدام ملفات تعريف الارتباط وعلامات الصفحة معًا، نستطيع تحسين خدمة الزا بوت وقياس فعالية حملاتنا الإعلانية والتسويق. يرجى أن تكون على علم أن سياسة ملفات الارتباط هذه لا تحكم استخدام مواقع الويب أو خدمات الطرف الثالث أو مقدمي الخدمات من الطرف الثالث.
</p>
